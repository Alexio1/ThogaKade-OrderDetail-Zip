    private void txtorderIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtorderIdActionPerformed
        try {
            String id = txtorderId.getText();
            
            String SQL = "Select * from orderdetail where txtorderId=?";
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection connection = DriverManager.getConnection("jdbc:mysql://localHost/ThogaKade", "root", "1234");
            PreparedStatement stm;
            stm = connection.prepareStatement(SQL);
            stm.setObject(1,txtorderId);
            
            ResultSet rst = stm.executeQuery();
            
            if (rst.next()) {
                txtitemCode.setText(rst.getString(" txtitemCode"));
                  txtqty.setText(rst.getString("txtqty"));
                 txtunitPrice.setText(rst.getString("txtunitPrice"));
            }else{
                JOptionPane.showMessageDialog(this, "orderdetail not found...");
            }
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Driver not found...");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_txtorderIdActionPerformed

