    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        String SQL="Update orderdetail set txtitemCode  =?, txtqty=?, txtunitPrice=? where txtorderId=?";
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection connection = DriverManager.getConnection("jdbc:mysql://localHost/ThogaKade", "root", "1234");
            PreparedStatement stm = connection.prepareStatement(SQL);
            stm.setObject(1, txtitemCode.getText());
            stm.setObject(2,txtqty.getText());
            stm.setObject(3, txtunitPrice.getText());
            stm.setObject(4, txtorderId.getText());
            
            int x = stm.executeUpdate();
            
            if (x>0) {
                JOptionPane.showMessageDialog(this, "Updated..");
            }else{
                JOptionPane.showMessageDialog(this, "orderdetail not found...");
            }
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Driver not found...");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }        
        
    }//GEN-LAST:event_btnUpdateActionPerformed

